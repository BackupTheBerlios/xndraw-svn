<a href="http://developer.berlios.de/projects/xndraw/">Xndraw</a> is
software to design and visualise patterns by freehand
drawing. Currently it's possible to create a variety of tiling
wallpapers, and generate output in png and svg. Source paths can be
imported and saved using svg. <br>
<br>
The aim is be able to create a 'tiling
shape', and then further subdivide it , perhaps according to a
different rule. <br>
